// This is the library crate for the main hodei-artifacts-api binary.
// It can be used to share code within the main application if needed in the future.

pub fn add(left: usize, right: usize) -> usize {
    left + right
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}